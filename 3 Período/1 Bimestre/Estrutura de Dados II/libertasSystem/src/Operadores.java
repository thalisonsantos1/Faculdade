public class Operadores {

    public static void main(String[] args) {
        int a = 3;
        System.out.println(a); //3
        System.out.println(++a); //3+1 = 4
        System.out.println(a); //4
        System.out.println(a++); //4 => 4+1 = 5
        System.out.println(a); //5
        boolean b = a>6;
        System.out.println(b);
        b = a<6;
        System.out.println(b);
    }
}
