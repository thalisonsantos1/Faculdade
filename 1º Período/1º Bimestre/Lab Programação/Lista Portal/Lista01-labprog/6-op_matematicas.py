num1 = float(input("Digite o primeiro número: "))
num2 = float(input("Digite o segundo número: "))

soma = (num1 + num2)
subtracao = (num1 - num2)
multiplicacao = (num1 * num2)
divisao = (num1 / num2)

print (f"O resultado da soma é {soma:.2f}!")
print (f"O resultado da subtracao é {subtracao:.2f}!")
print (f"O resultado da multiplicacao é {multiplicacao:.2f}!")
print (f"O resultado da divisao é {divisao:.2f}!")